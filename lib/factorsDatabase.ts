export type LocalKey = keyof typeof LOCALS;

export function generateFactors(localKey: LocalKey) {
  return {
    ...BASE,
    ...(LOCALS[localKey] || {}),
  };
}

function normalizeLabelForLookup(s: string): string {
  return s
    .trim()
    .toLowerCase()
    .normalize("NFD")
    .replace(/[\u0300-\u036f]/g, "")
    .replace(/\s+/g, " ");
}

