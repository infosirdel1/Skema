/** Facteurs AA→CB pour un libellé de local (DEFAULT_FACTORS si type inconnu). */
export function getFactorsForLocalLabel(label: string) {
  const norm = normalizeLabelForLookup(label);
  const key = LABEL_TO_LOCAL_KEY[norm];
  const fromPreset =
    key && key in LOCALS ? generateFactors(key) : { ...DEFAULT_FACTORS };
  return { ...DEFAULT_FACTORS, ...fromPreset };
}

export type FactorsRow = ReturnType<typeof generateFactors> & {
  local: string;
  public: boolean;
};
